void reset(Context *ctx, std::mt19937& engine) {
    for (int i = 0; i < ctx->ni; i++) {
	for (int j = 0; j < ctx->nk; j++) {
	    ctx->A[i][j] = ((i*j+1) % ctx->ni) * 1.0 / (5 * ctx->ni);
	}
    }

    for (int i = 0; i < ctx->nk; i++) {
	for (int j = 0; j < ctx->nj; j++) {
	    ctx->B[i][j] = ((i*(j+1)+2) % ctx->nj) * 1.0 / (5 * ctx->nj);
	}
    }

    for (int i = 0; i < ctx->nj; i++) {
	for (int j = 0; j < ctx->nm; j++) {
	    ctx->C[i][j] = (i*(j+3) % ctx->nl) * 1.0 / (5 * ctx->nl);
	}
    }

    for (int i = 0; i < ctx->nm; i++) {
	for (int j = 0; j < ctx->nl; j++) {
	    ctx->D[i][j] = ((i*(j+2)+2) % ctx->nk) * 1.0 / (5 * ctx->nk);
	}
    }
}


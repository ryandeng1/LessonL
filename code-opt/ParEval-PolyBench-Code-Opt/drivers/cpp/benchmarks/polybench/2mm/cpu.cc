bool validate(Context *ctx, std::mt19937& engine) {
    const size_t TEST_SIZE = DRIVER_PROBLEM_SIZE;

    int ni = TEST_SIZE;
    int nj = TEST_SIZE / 2;
    int nk = TEST_SIZE / 4;
    int nl = TEST_SIZE / 8;

    std::vector<std::vector<double>> A;
    std::vector<std::vector<double>> B;
    std::vector<std::vector<double>> C;
    std::vector<std::vector<double>> tmp, tmp_test;

    std::vector<std::vector<double>> D_correct;
    std::vector<std::vector<double>> D_test;

    A.resize(ni);
    B.resize(nk);
    C.resize(nj);
    tmp.resize(ni);
    tmp_test.resize(ni);

    D_correct.resize(ni);
    D_test.resize(ni);

    for (int i = 0; i < ni; i++) {
	A[i].resize(nk);
    }

    for (int i = 0; i < nk; i++) {
	B[i].resize(nj);
    }

    for (int i = 0; i < nj; i++) {
	C[i].resize(nl);
    }

    for (int i = 0; i < ni; i++) {
	D_correct[i].resize(nl);
	D_test[i].resize(nl);
    }

    for (int i = 0; i < ni; i++) {
	tmp[i].resize(nj);
	tmp_test[i].resize(nj);
    }

    double alpha = 1.5;
    double beta = 1.2;

    // set up input
    for (int i = 0; i < ni; i++) {
	for (int j = 0; j < nk; j++) {
	    A[i][j] = ((i*j+1) % ni) * 1.0 / ni;
	}
    }

    for (int i = 0; i < nk; i++) {
	for (int j = 0; j < nj; j++) {
	    B[i][j] = (i*(j+1) % nj) * 1.0 / nj;
	}
    }

    for (int i = 0; i < nj; i++) {
	for (int j = 0; j < nl; j++) {
	    C[i][j] = ((i*(j+3)+1) % nl) * 1.0 / nl;
	}
    }

    for (int i = 0; i < ni; i++) {
	for (int j = 0; j < nl; j++) {
	    D_correct[i][j] = (i*(j+2) % nk) * 1.0 / nk;
	    D_test[i][j] = D_correct[i][j];
	}
    }

    // compute correct result
    correct_kernel_2mm(ni, nj, nk, nl, alpha, beta, tmp, A, B, C, D_correct);

    // compute test result
    submission::kernel_2mm(ni, nj, nk, nl, alpha, beta, tmp_test, A, B, C, D_test);
        
    bool isCorrect = true;
    for (int i = 0; i < ni; i++) {
	if (!fequal(D_correct[i], D_test[i], 1e-4)) {
	    isCorrect = false;
	}
    }

    if (!isCorrect) {
        return false;
    }

    return true;
}

